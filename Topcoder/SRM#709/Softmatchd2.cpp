#include <bits/stdc++.h>

using namespace std;

class Softmatchd2 {
public:
	int count(string, string);
};

int Softmatchd2::count(string S, string pattern) {
	int n=S.size();
	int m=pattern.size();
	int mat[55][55];
	memset(mat, -1, sizeof(mat));
	for(int i=0;i+m<=n;i++){
		for(int j=0;j<m;j++){
			if(S[i+j]=='a'){
				if(pattern[j]=='0'||pattern[j]=='1')
					mat[i][i+j]=0;
				else
					mat[i][i+j]=1;
			}
			else{
				if(pattern[j]=='0'||pattern[j]=='2')
					mat[i][i+j]=0;
				else
					mat[i][i+j]=1;
			}
		}
	}
	for(int i=0;i<n-m+1;i++){
		for(int j=0;j<n;j++)
			printf("%d ", mat[i][j]);
		printf("\n");
	}

	return 0;
}


double test0() {
	string p0 = "aba";
	string p1 = "13";
	Softmatchd2 * obj = new Softmatchd2();
	clock_t start = clock();
	int my_answer = obj->count(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 2;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
